// C L A S S E S ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    class MVC
    {
        // P R O C E D U R E S //////////////////////////////////////////////////////////////////////////////////////////////
            protected static function extractParametersFromArgv()
            {
                global $argv;

                $args = array_slice($argv, 2);


                $p = array();
                foreach ($args AS $k => $v) {

                    if (  0 === strpos($v, '--')  ) {

                        $s = substr($v, 2);
                        if (  FALSE !== strpos($s, '=')  ) {

                            list($name, $value) = explode('=', $s, 2);
                            $p[$name] = $value;

                        } else {
                            $p[$s] = TRUE;
                        }


                    } else {
                        $p[] = $v;
                    }

                } // foreach

                return $p;
            }
        ////////////////////////////////////////////////////////////////////////////////////////////// P R O C E D U R E S //



        // M E T H O D S ////////////////////////////////////////////////////////////////////////////////////////////////////
            public function install()
            {
                _upgrade();
                _install();
            }

        //=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//

            public function upgrade()
            {
                _upgrade();
            }

        //=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//

            public function uninstall()
            {
                _uninstall();
            }

        //=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//

            public function createModule()
            {
                global $argv;

                _create_module($argv[2]);
            }

        //=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//

            public function createMod()
            {
                $this->createModule();
            }

        //=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//

            public function generateSQL()
            {
                $p = self::extractParametersFromArgv();

                _generate_sql($p);
            }

        //=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=//

            public function generateModel()
            {
                $p = self::extractParametersFromArgv();

                _generate_model($p);
            }

        //////////////////////////////////////////////////////////////////////////////////////////////////// M E T H O D S //

    } // class MVC
